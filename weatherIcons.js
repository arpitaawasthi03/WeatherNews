export function getIcon(name, customClass = "") {
  const svg = ICONS[name] || ICONS.cloud;
  if (customClass) {
    return svg.replace(/class="([^"]*)"/, `class="$1 ${customClass}"`);
  }
  return svg;
}

export function getWeatherIconByWmoCode(code) {
  // WMO weather code mapping to ICONS keys
  const numericCode = Number(code);
  if (numericCode === 0) return "sun"; // Clear sky
  if ([1, 2, 3].includes(numericCode)) return "cloud"; // Mainly clear, partly cloudy, overcast
  if ([45, 48].includes(numericCode)) return "cloud"; // Foggy
  if ([51, 53, 55, 56, 57].includes(numericCode)) return "cloud-drizzle"; // Drizzle
  if ([61, 63, 65, 66, 67, 80, 81, 82].includes(numericCode)) return "cloud-rain"; // Rain/Showers
  if ([71, 73, 75, 77, 85, 86].includes(numericCode)) return "snowflake"; // Snow
  if ([95, 96, 99].includes(numericCode)) return "cloud-lightning"; // Thunderstorm
  return "cloud";
}

export function getWeatherDescByWmoCode(code) {
  const numericCode = Number(code);
  if (numericCode === 0) return "Clear Skies";
  if (numericCode === 1) return "Mainly Clear";
  if (numericCode === 2) return "Partly Cloudy";
  if (numericCode === 3) return "Overcast";
  if (numericCode === 45 || numericCode === 48) return "Foggy";
  if (numericCode === 51 || numericCode === 53 || numericCode === 55) return "Drizzle";
  if (numericCode === 61 || numericCode === 63 || numericCode === 65) return "Rainy";
  if (numericCode === 66 || numericCode === 67) return "Freezing Rain";
  if (numericCode === 71 || numericCode === 73 || numericCode === 75) return "Snowy";
  if (numericCode === 77) return "Snow Grains";
  if (numericCode === 80 || numericCode === 81 || numericCode === 82) return "Rain Showers";
  if (numericCode === 85 || numericCode === 86) return "Snow Showers";
  if (numericCode === 95 || numericCode === 96 || numericCode === 99) return "Thunderstorm";
  return "Cloudy";
}
